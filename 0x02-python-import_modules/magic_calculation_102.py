#!/usr/bin/python3
def add(a, b):
    """My addition

    Args:
           a: first integer
           b: second integer

    Returns:
           The return value. a + b
    """
    return a + b


def sub(a, b):
    """My subtraction

    Args:
           a: first integer
           b: second integer

    Returns:
           The return value. a - b
    """
    return a - b
